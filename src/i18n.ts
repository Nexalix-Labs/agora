const STR: Record<string, Partial<Record<Key, string>>> = {
  en: EN, ru: RU, uk: UK, de: DE, es: ES, fr: FR, it: IT,
  pt: PT, pl: PL, tr: TR, zh: ZH, ja: JA, ko: KO,
  ar: AR, fa: FA, id: ID, hi: HI,
};

export function t(lang: string, key: Key): string {
  return STR[lang]?.[key] ?? EN[key];
}

/** Язык из настроек, иначе — из системной локали, иначе en. */
export function resolveLang(setting?: string): string {
  if (setting && STR[setting]) return setting;
  const nav = (navigator.language || "en").toLowerCase();
  const two = nav.slice(0, 2);
  return STR[two] ? two : "en";
}
